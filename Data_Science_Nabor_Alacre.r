complete <- function(directory, id = 1:332){
  if (directory == "specdata") {
    for (i in id) {
      x <- a331[a331$ID == i, ]
      y <- is.na(x$sulfate)
      yy <- x[!y,]
      z <- is.na(x$nitrate)
      zz <- x[!z,]
      nobs_sulf <- nrow(yy)
      nobs_nit <- nrow(zz)
      if (nobs_sulf < nobs_nit ){
        nobs <- nobs_sulf
        id <- i
        output <- data.frame(id,nobs)
      } else if (nobs_nit < nobs_sulf) {
        nobs <- nobs_nit
        id <- i
        output <- data.frame(id,nobs)
      } else {
        nobs <- nobs_sulf
        id <- i
        output <- data.frame(id,nobs)
      }
      print(output)
    } 
  }
  else {
    print("ERROR")
  }
}


