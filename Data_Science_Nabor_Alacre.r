pollutantmean <- function(directory, pollutant, id = 1:332) {
    if (directory == "specdata"){
        if (pollutant == "sulfate"){
           sum_num = 0
           sum_den = 0
           for (i in id) {
              x <- a331[a331$ID == i, ]
              y <- is.na(x$sulfate)
              z <- x[!y,]
              numerator <- sum(z$sulfate)
              denominator <- nrow(z)
              sum_num = sum_num + numerator
              sum_den = sum_den + denominator
           }
        mean = sum_num / sum_den
        print(mean)
        }
        else if (pollutant == "nitrate") {
           sum_num = 0
           sum_den = 0
           for (i in id) {
              x <- a331[a331$ID == i, ]
              y <- is.na(x$nitrate)
              z <- x[!y,]
              numerator <- sum(z$nitrate)
              denominator <- nrow(z)
              sum_num = sum_num + numerator
              sum_den = sum_den + denominator
           }
        mean = sum_num / sum_den
        print(mean)
        }
        else {
           print("ERROR")
        }
    }  
    else {
      print("ERROR")
    } 
}


