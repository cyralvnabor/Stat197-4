corr <- function(directory, threshold = 0) {
  x <- complete.cases(a331)
  xx <- a331[x,] 
  prev_cor = NULL
  for ( i in 1:332) { 
    if (nrow(xx[xx$ID == i,]) >= threshold){
      sulf <-  xx[xx$sulfate == i,]
      nitr <- xx[xx$nitrate == i,]
      cor(sulf, nitr)
      output <-  c(prev_cor, cor(sulf, nitr))
    }
  }
  
  output
}
